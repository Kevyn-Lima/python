# Reforçando operdores de atribuição

#Atribuição simples
saldo = 500 

#Atribuição com adição
saldo += 200

print(saldo)

#Atribuição com subtração, divisão, Multiplicação, Modulo
saldo -= 100
print(saldo)

saldo *= 2
print(saldo)

saldo /= 5
print(saldo)

saldo //= 5
print(saldo)

saldo = 500
print(saldo)

saldo %= 480
print(saldo)

#Atribuição com exponenciação
saldo **= 2
print(saldo)